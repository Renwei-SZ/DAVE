#include "dave_tools.h"

static ub _rand;

// =====================================================================

ub
t_rand(void)
{
	static ub out_put_rand;

	_rand = _rand + rand();

	if(out_put_rand == _rand)
	{
		_rand = _rand + 1;
	}

	out_put_rand = _rand;

	return out_put_rand;
}

